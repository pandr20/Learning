package ForEeach;

public class Loop {


    public static void main(String[] args) {
// for(initialization; condition ; increment/decrement)
        for(int i = 0; i < 10; i++) {
            System.out.println(i);
        }
        for(int i = 0; i < 11; i++) {
            System.out.println(i + " Patrick is awesome at programming");
        }
    }
}
